#ifndef MODBUSRTU_H
#define MODBUSRTU_H

#include <QByteArray>
#include <QList>
#include <QString>
#include <cstdint>

/*
 * 实现了一个简易的 Modbus RTU 主站 工具类
 * 专门用于生成 功能码 03（读保持寄存器） 的请求帧，并解析对应的响应帧
 */

struct ModbusResponse
{
    bool valid {false}; // 标识整个解析过程是否成功（无 CRC 错误、无异常码、无数据缺损等）。
    uint8_t slaveId {0}; // 从站地址
    uint8_t funCode {0}; // 功能码
    QList<uint16_t> registers; // 解析出的寄存器值列表
    QString errorMsg; // 如果解析失败返回错误信息，成功则为空串
};

class ModbusRTU
{
public:
    // 禁止实例化
    ModbusRTU() = delete;

    // 构造 FC03 （读保持寄存器）请求帧
    // =============================================================================
    // Modbus 0x03 请求帧格式
    // 字段         长度       说明
    // -----------------------------------------------------------------------------
    // 从站地址      1 字节     slaveId
    // 功能码        1 字节     0x03
    // 起始地址      2 字节     startAddr（大端序）
    // 寄存器数量     2 字节     count（大端序）
    // CRC 校验      2 字节     对前 6 字节计算所得（小端序放在帧尾）
    // =============================================================================
    static QByteArray buildReadHoldingRegisters(uint8_t slaveId,
                                                uint16_t startAddr,
                                                uint16_t count);

    // 解析 FC03 响应帧 -> 寄存器列表
    // =============================================================================
    // Modbus 0x03 正常响应帧格式
    // 字段         长度      说明
    // -----------------------------------------------------------------------------
    // 从站地址      1        slaveId
    // 功能码        1        0x03
    // 字节数        1        byteCount（寄存器数量 × 2）
    // 寄存器数据     N        大端序的寄存器值序列
    // CRC 校验      2        对前面所有字节的 CRC
    // =============================================================================

    // =============================================================================
    // Modbus 0x03 异常响应帧格式
    // 字段         长度      说明
    // -----------------------------------------------------------------------------
    // 从站地址      1        slaveId
    // 功能码        1        0x83（原功能码最高位置 1）
    // 异常码        1        如 0x01、0x02 等
    // CRC 校验      2
    // =============================================================================
    static ModbusResponse parseReadResponse(const QByteArray &frame);
};

#endif // MODBUSRTU_H
